from __future__ import annotations

import argparse
import json
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Evaluate prediction JSON with LoCoMo's official evaluation functions."
    )
    parser.add_argument(
        "--ann-file",
        type=str,
        default="/share/home/leiyh5/Memory/data/locomo/locomo_qa_test.json",
        help="Annotated LoCoMo subset file with ground-truth answers and conversations.",
    )
    parser.add_argument(
        "--pred-file",
        type=str,
        default="/share/home/leiyh5/Memory/data/locomo/locomo_qa_test_pred_category.json",
        help="Prediction file generated by your inference pipeline.",
    )
    parser.add_argument(
        "--prediction-key",
        type=str,
        default="memory_prediction",
        help="Field name in each qa item that stores model predictions.",
    )
    parser.add_argument(
        "--model-key",
        type=str,
        default="memory",
        help="Prefix for per-question score key (e.g., memory -> memory_f1).",
    )
    parser.add_argument(
        "--locomo-root",
        type=str,
        default="/share/home/leiyh5/locomo",
        help="Path to the LoCoMo repository root.",
    )
    parser.add_argument(
        "--scored-file",
        type=str,
        default="/share/home/leiyh5/Memory/data/locomo/locomo_qa_test_pred_scored.json",
        help="Output path for predictions with per-question scores added.",
    )
    parser.add_argument(
        "--stats-file",
        type=str,
        default="/share/home/leiyh5/Memory/data/locomo/locomo_qa_test_pred_stats.json",
        help="Output path for aggregate statistics JSON.",
    )
    return parser.parse_args()


def _load_json(path: Path) -> Any:
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def _dump_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(payload, f, ensure_ascii=False, indent=2)


def _print_category_stats(samples: list[dict[str, Any]], metric_key: str) -> None:
    category_names = {
        1: "multi-hop retrieval",
        2: "temporal reasoning",
        3: "open-domain knowledge",
        4: "single-hop retrieval",
        5: "adversarial",
    }
    score_sums: dict[int, float] = defaultdict(float)
    counts: dict[int, int] = defaultdict(int)

    for sample in samples:
        for qa in sample.get("qa", []):
            category = qa.get("category")
            try:
                category_int = int(category)
            except (TypeError, ValueError):
                continue
            if category_int not in category_names:
                continue
            counts[category_int] += 1
            if metric_key in qa:
                score_sums[category_int] += float(qa[metric_key])

    print("\nCategory stats (ordered 1 -> 5):")
    total_count = 0
    total_score = 0.0
    for category in (1, 2, 3, 4, 5):
        count = counts[category]
        score_sum = score_sums[category]
        avg = (score_sum / count) if count > 0 else 0.0
        total_count += count
        total_score += score_sum
        print(
            f"  {category}. {category_names[category]}: "
            f"count={count}, avg_f1={avg:.3f}"
        )

    overall = (total_score / total_count) if total_count > 0 else 0.0
    print(f"  overall: count={total_count}, avg_f1={overall:.3f}")


def main() -> None:
    args = parse_args()

    ann_path = Path(args.ann_file)
    pred_path = Path(args.pred_file)
    scored_path = Path(args.scored_file)
    stats_path = Path(args.stats_file)
    locomo_root = Path(args.locomo_root)

    if not ann_path.is_file():
        raise FileNotFoundError(f"Annotation file not found: {ann_path}")
    if not pred_path.is_file():
        raise FileNotFoundError(f"Prediction file not found: {pred_path}")
    if not locomo_root.is_dir():
        raise FileNotFoundError(f"LoCoMo repo not found: {locomo_root}")

    if str(locomo_root) not in sys.path:
        sys.path.insert(0, str(locomo_root))

    try:
        from task_eval.evaluation import eval_question_answering
        from task_eval.evaluation_stats import analyze_aggr_acc
    except ImportError as e:
        raise ImportError(
            "Failed to import LoCoMo evaluation modules. "
            "Install dependencies first, e.g. `pip install regex nltk rouge bert-score`."
        ) from e

    prediction_key = args.prediction_key
    model_key = args.model_key
    metric_key = f"{model_key}_f1"

    samples = _load_json(pred_path)
    ann_samples = _load_json(ann_path)
    if not isinstance(samples, list):
        raise ValueError("Prediction file must be a JSON list of samples.")
    if not isinstance(ann_samples, list):
        raise ValueError("Annotation file must be a JSON list of samples.")

    ann_by_id: dict[str, dict[str, Any]] = {}
    for ann_sample in ann_samples:
        sid = str(ann_sample.get("sample_id", ""))
        if sid:
            ann_by_id[sid] = ann_sample

    total_qa = 0
    missing_pred = 0
    filled_required = 0
    filled_from_adversarial = 0
    for sample in samples:
        sid = str(sample.get("sample_id", ""))
        qas = sample.get("qa", [])
        total_qa += len(qas)
        ann_qas = ann_by_id.get(sid, {}).get("qa", []) if sid else []
        for i, qa in enumerate(qas):
            # LoCoMo category-5 items in released data often use `adversarial_answer`
            # instead of `answer`, while official evaluator expects `answer`.
            if "answer" not in qa and "adversarial_answer" in qa:
                qa["answer"] = ann_qas[i].get("adversarial_answer", qa["adversarial_answer"]) if i < len(ann_qas) else qa["adversarial_answer"]
                filled_from_adversarial += 1

            # Backfill required keys from annotation data if missing.
            if i < len(ann_qas):
                ann_qa = ann_qas[i]
                for required_key in ("answer", "category", "evidence"):
                    if required_key not in qa:
                        if required_key == "answer" and "adversarial_answer" in ann_qa:
                            qa[required_key] = ann_qa["adversarial_answer"]
                        else:
                            qa[required_key] = ann_qa.get(required_key)
                        filled_required += 1

            if prediction_key not in qa:
                missing_pred += 1
                qa[prediction_key] = ""

    if missing_pred > 0:
        print(f"[warning] {missing_pred} QA items missing `{prediction_key}`. Filled with empty strings.")
    if filled_required > 0:
        print(
            f"[warning] Backfilled {filled_required} missing required fields "
            f"(`answer`/`category`/`evidence`) from annotation file."
        )
    if filled_from_adversarial > 0:
        print(
            f"[info] Filled {filled_from_adversarial} missing `answer` fields from `adversarial_answer`."
        )

    for sample in samples:
        qas = sample.get("qa", [])
        scores, _, _ = eval_question_answering(qas, prediction_key)
        for i, score in enumerate(scores):
            qas[i][metric_key] = round(float(score), 3)

    _dump_json(scored_path, samples)

    analyze_aggr_acc(
        str(ann_path),
        str(scored_path),
        str(stats_path),
        model_key,
        metric_key,
        rag=False,
    )
    _print_category_stats(samples, metric_key)

    print("\nEvaluation completed.")
    print(f"Total QA evaluated: {total_qa}")
    print(f"Per-question scored file: {scored_path}")
    print(f"Aggregate stats file: {stats_path}")


if __name__ == "__main__":
    main()

